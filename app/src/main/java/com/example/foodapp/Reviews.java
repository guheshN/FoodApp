package com.example.foodapp;

public class Reviews {
    private String name;
    private String review;
    private Double score;

    public  String getName() {
        return name;
    }
    public void setName(){
        this.name = name;
    }

    public String getReview(){
        return review;
    }

    public void setReview(){
        this.review = review;
    }

    public Double getScore(){
        return score;
    }

    public void setScore(){
        this.score= score;
    }


}
